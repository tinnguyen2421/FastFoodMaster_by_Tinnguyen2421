package com.example.appfood_by_tinnguyen2421.Chef.ChefModel;

public class ChefVoucher {
    private String StartDate,EndDate,VoucherID,VoucherName,VoucherApply,VoucherValue,DishUseVoucher;

    public ChefVoucher() {
    }

    public String getStartDate() {
        return StartDate;
    }

    public void setStartDate(String startDate) {
        StartDate = startDate;
    }

    public String getEndDate() {
        return EndDate;
    }

    public void setEndDate(String endDate) {
        EndDate = endDate;
    }

    public String getVoucherID() {
        return VoucherID;
    }

    public void setVoucherID(String voucherID) {
        VoucherID = voucherID;
    }

    public String getVoucherName() {
        return VoucherName;
    }

    public void setVoucherName(String voucherName) {
        VoucherName = voucherName;
    }

    public String getVoucherApply() {
        return VoucherApply;
    }

    public void setVoucherApply(String voucherApply) {
        VoucherApply = voucherApply;
    }

    public String getVoucherValue() {
        return VoucherValue;
    }

    public void setVoucherValue(String voucherValue) {
        VoucherValue = voucherValue;
    }

    public String getDishUseVoucher() {
        return DishUseVoucher;
    }

    public void setDishUseVoucher(String dishUseVoucher) {
        DishUseVoucher = dishUseVoucher;
    }
}
